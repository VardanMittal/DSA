#include <bits/stdc++.h>
using namespace std;

int main(){
    // learning For loop
    for(int i=0; i<5; i++){ 
        // for(initialization; condition; increment/decrement)
        cout << "Vardan Mittal" << endl;
    }

    // learning while loop
    int j = 0;
    while(j<5){
        // while(condition){ 
             // code
             // increment/decrement ** must be there}
        cout << "Vardan Mittal" << endl;
        j++;
    }

    // learning do while loop must run atleast once
    int k = 0;
    do{
        // do{
            // code
            // increment/decrement ** must be there
        // }while(condition);
        cout << "Vardan Mittal" << endl;
        k++;
    }while(k<5);

    return 0;
}